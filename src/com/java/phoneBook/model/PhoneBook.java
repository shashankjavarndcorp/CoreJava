package com.java.phoneBook.model;

public class PhoneBook {
	private int id;
	private String name;
	private long mobile;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	public long getMobile() {
		return mobile;
	}
	public void setMobile(long mobile) {
		this.mobile = mobile;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

}

